package enums;

import java.awt.event.KeyEvent;

public enum Keyboard {

    UP(KeyEvent.VK_UP),
    DOWN(KeyEvent.VK_DOWN),
    LEFT(KeyEvent.VK_LEFT),
    RIGHT(KeyEvent.VK_RIGHT);

    private final Integer value;

    Keyboard(Integer value) {
        this.value = value;
    }

}
